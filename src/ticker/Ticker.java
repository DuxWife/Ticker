    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        s=jTextField1.getText();
        v=(int)jSpinner1.getValue(); size=(int)jSpinner2.getValue(); 
        j=(jPanel1.getHeight()+size)/2;  
        g = jPanel1.getGraphics();
        f=g.getFont();
        g.setFont(new Font(f.getFontName(),f.getStyle(),size));
        fm=g.getFontMetrics();        
        g.clearRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
        i=-fm.stringWidth(s);
        timer.setDelay(1000/v);
        timer.start();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        timer.stop();
    }//GEN-LAST:event_jButton2ActionPerformed
private void draw(Graphics g) {
    g.clearRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
    if (jComboBox1.getSelectedItem().equals((Object)"ВПРАВО")){                 
                g.drawString(s, i, j);
                i++;
                if (i>jPanel1.getWidth()) i=-fm.stringWidth(s);
        } else {
                g.drawString(s, i, j);
                i--;
                if (i<=-fm.stringWidth(s)) i= jPanel1.getWidth();
    }
}
